/**
 * Normalize a single habitat string to its canonical form.
 * Returns null if unmapped (should not happen after migration).
 */
export const normalizeHabitat = (habitat) => {
  const canonical = HABITAT_ALIASES[habitat];
  if (!canonical) {
    console.warn(`Unmapped habitat: "${habitat}"`);
    return habitat;
  }
  return canonical;
};

/**
 * Normalize an array of habitats: map aliases, dedupe, sort by canonical order.
 */
export const normalizeHabitats = (habitats) => {
  const order = Object.fromEntries(
    CANONICAL_HABITATS.map((h, i) => [h, i])
  );
  const unique = [...new Set(habitats.map(normalizeHabitat))];
  return unique.sort((a, b) => (order[a] ?? 999) - (order[b] ?? 999));
};
